package th.co.maximus.bean;

public class MasterDataBean {
	
	private int id;
	private String value;
	private String text;
	private String group;
	private String masterdataGroup;
	private String serviceType;
	private String serviceDepartment;
	private String serviceName;
	private String category;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getGroup() {
		return group;
	}
	public void setGroup(String group) {
		this.group = group;
	}
	public String getMasterdataGroup() {
		return masterdataGroup;
	}
	public void setMasterdataGroup(String masterdataGroup) {
		this.masterdataGroup = masterdataGroup;
	}
	public String getServiceType() {
		return serviceType;
	}
	public void setServiceType(String serviceType) {
		this.serviceType = serviceType;
	}
	public String getServiceDepartment() {
		return serviceDepartment;
	}
	public void setServiceDepartment(String serviceDepartment) {
		this.serviceDepartment = serviceDepartment;
	}
	public String getServiceName() {
		return serviceName;
	}
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	
	
	

}
